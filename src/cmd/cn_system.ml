(* === Runtime === *)

let update_runtime hub_path =
  let runtime_path = Cn_ffi.Path.join hub_path "state/runtime.md" in
  let cn_ver = version in
  let hub_name = derive_name hub_path in
  let hub_commit = match Cn_ffi.Child_process.exec_in ~cwd:hub_path "git rev-parse --short HEAD 2>/dev/null" with
    | Some c -> String.trim c
    | None -> "unknown"
  in
  let cnos_commit = Cn_lib.cnos_commit in
  let peers_path = Cn_ffi.Path.join hub_path "state/peers.md" in
  let peer_count = match Cn_ffi.Fs.exists peers_path with
    | true ->
        let content = Cn_ffi.Fs.read peers_path in
        let lines = String.split_on_char '\n' content in
        lines |> List.filter (fun l -> String.length l > 2 && String.sub l 0 2 = "- ") |> List.length
    | false -> 0
  in
  (* Read installed package versions for drift detection *)
  let pkg_versions =
    let installed = Cn_assets.list_installed_packages hub_path in
    installed |> List.map (fun (_name, pkg_path) ->
      let dir_name = Filename.basename pkg_path in
      match String.index_opt dir_name '@' with
      | Some i ->
        let n = String.sub dir_name 0 i in
        let v = String.sub dir_name (i + 1) (String.length dir_name - i - 1) in
        Printf.sprintf "%s@%s" n v
      | None -> dir_name)
  in
  let pkg_line = match pkg_versions with
    | [] -> "packages: (none)"
    | vs -> "packages: " ^ String.concat ", " vs
  in
  let drift = pkg_versions |> List.exists (fun pv ->
    match String.index_opt pv '@' with
    | Some i ->
      let v = String.sub pv (i + 1) (String.length pv - i - 1) in
      v <> cn_ver
    | None -> false)
  in
  let content = Printf.sprintf {|# Runtime State

Auto-generated by cn. Do not edit manually.

```yaml
last_sync: %s
cn_version: %s
cnos_commit: %s
hub_name: %s
hub_commit: %s
peer_count: %d
%s
version_drift: %b
```
|} (Cn_fmt.now_iso ()) cn_ver cnos_commit hub_name hub_commit peer_count pkg_line drift in
  Cn_ffi.Fs.write runtime_path content;
  print_endline (Cn_fmt.ok "Updated state/runtime.md")

(* === Status === *)

let run_status hub_path name =
  print_endline (Cn_fmt.info (Printf.sprintf "cn hub: %s" name));
  print_endline "";
  Printf.printf "hub..................... %s\n" (Cn_fmt.green Cn_fmt.check);
  Printf.printf "name.................... %s %s\n" (Cn_fmt.green Cn_fmt.check) name;
  Printf.printf "path.................... %s %s\n" (Cn_fmt.green Cn_fmt.check) hub_path;
  (* Package version display + drift detection *)
  let installed = Cn_assets.list_installed_packages hub_path in
  let has_drift = ref false in
  installed |> List.iter (fun (_pkg_name, pkg_path) ->
    let dir_name = Filename.basename pkg_path in
    match String.index_opt dir_name '@' with
    | Some i ->
      let n = String.sub dir_name 0 i in
      let v = String.sub dir_name (i + 1) (String.length dir_name - i - 1) in
      let ok = v = version in
      if not ok then has_drift := true;
      let status = if ok then Cn_fmt.green (Cn_fmt.check ^ " " ^ v)
        else Cn_fmt.red (Printf.sprintf "%s %s (expected %s)" Cn_fmt.cross v version) in
      Printf.printf "%s................. %s\n" n status
    | None -> ());
  print_endline "";
  if !has_drift then
    print_endline (Cn_fmt.warn (Printf.sprintf "[status] version_drift version=%s" version))
  else
    print_endline (Cn_fmt.dim (Printf.sprintf "[status] ok version=%s" version))

