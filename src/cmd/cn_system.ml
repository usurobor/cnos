(** cn sync — intentionally narrower than maintain_once: runs only the
    transport primitives (inbox check, peer sync with heartbeat, outbox flush)
    plus a runtime projection update.  Omits review, update-check, cleanup,
    and inbox materialization so that `cn sync` stays a quick user-invokable
    transport round-trip rather than a full maintenance tick.
    Delegates to Cn_maintenance primitives to avoid divergence. *)
let run_sync hub_path name =
  print_endline (Cn_fmt.info "Syncing...");
  let _ = Cn_maintenance.inbox_check_once ~hub_path ~name in
  let _ = Cn_maintenance.sync_once ~hub_path in
  let _ = Cn_maintenance.flush_outbox_once ~hub_path ~name in
  update_runtime hub_path;
  print_endline (Cn_fmt.ok "Sync complete")

(* === Setup === *)

(** Read role from .cn/config.json runtime.role, default "engineer". *)
let read_role hub_path =
  let cfg = Cn_ffi.Path.join hub_path ".cn/config.json" in
  if not (Cn_ffi.Fs.exists cfg) then "engineer"
  else
    match Cn_ffi.Fs.read cfg |> Cn_json.parse with
    | Ok json ->
        (match Cn_json.get "runtime" json with
         | Some rt -> Cn_json.get_string "role" rt
           |> Option.map String.lowercase_ascii
           |> Option.value ~default:"engineer"
         | None -> "engineer")
    | Error _ -> "engineer"

(** Install cognitive packages into a hub: write deps manifest + lockfile,
    then restore packages. Called by both run_setup and run_init.
    No vendor/core — everything is a package. *)
let setup_assets hub_path =
  print_endline (Cn_fmt.info "Installing cognitive packages...");

  (* Write default manifest if missing *)
  let manifest =
    if Cn_ffi.Fs.exists (Cn_ffi.Path.join hub_path ".cn/deps.json") then
      (match Cn_deps.read_manifest ~hub_path with
       | Some m -> m
       | None ->
           let role = read_role hub_path in
           let m = Cn_deps.default_manifest_for_profile role in
           Cn_deps.write_manifest ~hub_path m;
           print_endline (Cn_fmt.ok (Printf.sprintf "Created .cn/deps.json (profile: %s)" role));
           m)
    else begin
      let role = read_role hub_path in
      let m = Cn_deps.default_manifest_for_profile role in
      Cn_deps.write_manifest ~hub_path m;
      print_endline (Cn_fmt.ok (Printf.sprintf "Created .cn/deps.json (profile: %s)" role));
      m
    end
  in

  (* Write lockfile from manifest (pins current rev + subdir) *)
  if not (Cn_ffi.Fs.exists (Cn_ffi.Path.join hub_path ".cn/deps.lock.json")) then begin
    let lock = Cn_deps.lockfile_for_manifest manifest in
    Cn_deps.write_lockfile ~hub_path lock;
    print_endline (Cn_fmt.ok "Created .cn/deps.lock.json")
  end;

  (* Restore from lockfile — MUST succeed for hub to be wake-ready *)
  (match Cn_deps.restore ~hub_path with
   | Ok () ->
       let summary = Cn_assets.summarize ~hub_path in
       let pkg_count = List.length summary.packages in
       print_endline (Cn_fmt.ok (Printf.sprintf
         "Assets ready: %d packages, %d doctrine, %d mindsets"
         pkg_count summary.doctrine_count summary.mindset_count))
   | Error msg ->
       print_endline (Cn_fmt.fail (Printf.sprintf "Restore failed: %s" msg));
       print_endline (Cn_fmt.fail "Hub is NOT wake-ready. Fix the issue and re-run 'cn setup'.");
       Cn_ffi.Process.exit 1)

(** cn setup — interactive hub setup.
    Makes any hub wake-ready: materializes core assets, writes default
    deps manifest with profile package, runs restore.
    Does NOT require root. System-level setup (logrotate, cron) is
    handled separately via cn setup --system. *)
let run_setup hub_path =
  print_endline (Cn_fmt.info (Printf.sprintf "Setting up hub: %s" hub_path));

  (* Ensure required directories exist *)
  Cn_ffi.Fs.ensure_dir (Cn_ffi.Path.join hub_path ".cn");
  Cn_ffi.Fs.ensure_dir (Cn_ffi.Path.join hub_path "agent");

  (* Materialize cognitive substrate *)
  setup_assets hub_path;

  (* Add .cn/vendor/ to .gitignore if not present *)
  let gitignore_path = Cn_ffi.Path.join hub_path ".gitignore" in
  let existing_gi = if Cn_ffi.Fs.exists gitignore_path
    then Cn_ffi.Fs.read gitignore_path else "" in
  if not (Cn_lib.starts_with ~prefix:".cn/vendor" (String.trim existing_gi))
     && not (Cn_assets.contains_sub existing_gi ".cn/vendor") then begin
    let content = existing_gi ^
      (if existing_gi <> "" && not (Cn_lib.ends_with ~suffix:"\n" existing_gi)
       then "\n" else "") ^ ".cn/vendor/\n" in
    Cn_ffi.Fs.write gitignore_path content;
    print_endline (Cn_fmt.ok "Added .cn/vendor/ to .gitignore")
  end;

  print_endline "";
  print_endline (Cn_fmt.ok "Hub setup complete!")

(* === Update === *)

let update_cron hub_path =
  let cron_line = Printf.sprintf "*/5 * * * * cn-cron %s" (Filename.quote hub_path) in
  print_endline (Cn_fmt.info "Updating crontab (5 min intervals)...");
  let cmd = Printf.sprintf "echo %s | crontab -" (Filename.quote cron_line) in
  match Cn_ffi.Child_process.exec cmd with
  | Some _ -> print_endline (Cn_fmt.ok "Crontab updated")
  | None -> print_endline (Cn_fmt.warn "Crontab update failed - update manually")

(** Reconcile first-party packages after a binary update.
    Rewrites lock entries to match the new runtime version, then restores. *)
let reconcile_packages hub_path =
  print_endline (Cn_fmt.info "Reconciling packages with new runtime...");
  let manifest = match Cn_deps.read_manifest ~hub_path with
    | Some m -> m
    | None ->
      let role = read_role hub_path in
      Cn_deps.default_manifest_for_profile role
  in
  let lock = Cn_deps.lockfile_for_manifest manifest in
  Cn_deps.write_lockfile ~hub_path lock;
  (match Cn_deps.restore ~hub_path with
   | Ok () ->
     print_endline (Cn_fmt.ok (Printf.sprintf "Packages reconciled to %s" version));
     update_runtime hub_path
   | Error msg ->
     print_endline (Cn_fmt.fail (Printf.sprintf "Package reconciliation failed: %s" msg)))

let run_update hub_path_opt =
  (* If re-exec'd after binary update, reconcile packages with new version and return *)
  (match Sys.getenv_opt "CN_RECONCILE_HUB" with
   | Some hp when hp <> "" ->
     reconcile_packages hp;
     (* Clear env var so downstream code doesn't see it *)
     Unix.putenv "CN_RECONCILE_HUB" ""
   | _ -> ());

  print_endline (Cn_fmt.info "Checking for updates...");
  print_endline (Printf.sprintf "Current version: %s" version);

  (* One update path: pre-built binaries from GitHub Releases.
     Mirrors install.sh — no git clone, no OCaml toolchain required.
     #37: detects same-version patches via commit hash comparison. *)
  match Cn_agent.get_latest_release () with
  | None ->
      print_endline (Cn_fmt.fail "Could not check for updates (GitHub API unreachable)");
      Cn_ffi.Process.exit 1
  | Some rel ->
      let update_info =
        if Cn_lib.is_newer_version rel.tag version then
          Some (Cn_agent.Update_available rel.tag,
                Printf.sprintf "New version available: %s" rel.tag)
        else if rel.commit <> "" && rel.commit <> Cn_lib.cnos_commit then
          Some (Cn_agent.Update_patch rel.tag,
                Printf.sprintf "Patch available: %s (commit %s → %s)"
                  rel.tag Cn_lib.cnos_commit rel.commit)
        else None
      in
      (match update_info with
      | None ->
          print_endline (Cn_fmt.ok (Printf.sprintf "Already up to date (%s %s)"
            version Cn_lib.cnos_commit))
      | Some (info, msg) ->
        print_endline (Cn_fmt.info msg);
        print_endline (Cn_fmt.info "Downloading pre-built binary...");
        let result = Cn_agent.do_update info in
        (match result with
        | Cn_protocol.Update_complete ->
            print_endline (Cn_fmt.ok (Printf.sprintf "Updated to %s" rel.tag));
            (* Re-exec into the new binary so reconciliation uses the correct
               version/commit constants. The new process detects CN_RECONCILE_HUB
               and reconciles before proceeding with its normal update check. *)
            (match hub_path_opt with
             | Some hp ->
               Unix.putenv "CN_RECONCILE_HUB" hp;
               Cn_agent.re_exec ()
             | None -> ())
        | _ ->
            print_endline (Cn_fmt.fail "Binary download failed. Check network and try again.")))

let run_update_with_cron hub_path =
  run_update (Some hub_path);
  update_cron hub_path;
  update_runtime hub_path;
  print_endline (Cn_fmt.info "Auto-committing runtime changes...");
  let _ = Cn_ffi.Child_process.exec_in ~cwd:hub_path "git add state/runtime.md" in
  let commit_result = Cn_ffi.Child_process.exec_in ~cwd:hub_path "git commit -m 'chore: cn update runtime'" in
  match commit_result with
  | Some _ ->
      print_endline (Cn_fmt.ok "Committed runtime changes");
      (match Cn_ffi.Child_process.exec_in ~cwd:hub_path "git remote get-url origin 2>/dev/null" with
       | Some _ ->
           print_endline (Cn_fmt.info "Pushing to origin...");
           (match Cn_ffi.Child_process.exec_in ~cwd:hub_path "git push origin HEAD 2>&1" with
            | Some _ -> print_endline (Cn_fmt.ok "Pushed to origin")
            | None -> print_endline (Cn_fmt.warn "Push failed - push manually"))
       | None -> print_endline (Cn_fmt.dim "No origin remote - skipping push"))
  | None -> print_endline (Cn_fmt.dim "No runtime changes to commit")

(* === Self-Update Check === *)

(* Guard re-entry: CN_UPDATE_RUNNING prevents infinite re-exec loop.
   See cn_agent.ml re_exec and MCA: self-update-recursion. *)
let self_update_check () =
  match Sys.getenv_opt "CN_UPDATE_RUNNING" with
  | Some _ -> ()
  | None ->
  if not (Cn_agent.auto_update_enabled ()) then ()
  else
  let args = Cn_ffi.Process.argv |> Array.to_list in
  let is_skip_cmd = List.exists (fun a ->
    a = "--help" || a = "-h" || a = "--version" || a = "-V" || a = "help"
  ) args in
  if is_skip_cmd then ()
  else
    match Cn_agent.get_latest_release () with
    | None -> ()
    | Some rel ->
        let info =
          if Cn_lib.is_newer_version rel.tag version then
            Some (Cn_agent.Update_available rel.tag,
                  Printf.sprintf "Updating cn %s → %s..." version rel.tag)
          else if rel.commit <> "" && rel.commit <> Cn_lib.cnos_commit then
            Some (Cn_agent.Update_patch rel.tag,
                  Printf.sprintf "Patch available: %s (%s → %s)"
                    rel.tag Cn_lib.cnos_commit rel.commit)
          else None
        in
        match info with
        | None -> ()
        | Some (update_info, msg) ->
            print_endline (Cn_fmt.info msg);
            let result = Cn_agent.do_update update_info in
            match result with
            | Cn_protocol.Update_complete ->
                print_endline (Cn_fmt.ok (Printf.sprintf "Updated to cn %s (%s)"
                  rel.tag rel.commit));
                Cn_agent.re_exec ()
            | _ ->
                print_endline (Cn_fmt.warn "Self-update failed - continuing with current version")

(* === Release === *)

let run_release hub_path_opt version_override =
  let ver = match version_override with
    | Some v -> v
    | None -> Cn_lib.version
  in
  let tag = "v" ^ ver in
  let install_dir = Cn_ffi.Process.cwd () in

  (* Ensure we're on main *)
  let branch = Git.current_branch ~cwd:install_dir |> Option.value ~default:"" in
  if branch <> "main" && branch <> "master" then begin
    print_endline (Cn_fmt.fail (Printf.sprintf "Must be on main branch (currently on %s)" branch));
    Cn_ffi.Process.exit 1
  end;

  (* Verify version consistency before tagging — release gate (#22) *)
  let version_ok =
    Cn_ffi.Child_process.exec_in ~cwd:install_dir
      "scripts/check-version-consistency.sh"
    |> Option.is_some
  in
  if not version_ok then begin
    print_endline (Cn_fmt.fail "Version consistency check failed — run 'scripts/stamp-versions.sh' then commit");
    Cn_ffi.Process.exit 1
  end;

  (* Check if tag already exists *)
  let tag_exists =
    Cn_ffi.Child_process.exec_in ~cwd:install_dir
      (Printf.sprintf "git tag -l %s" (Filename.quote tag))
    |> Option.map (fun s -> String.length (String.trim s) > 0)
    |> Option.value ~default:false
  in
  if tag_exists then begin
    print_endline (Cn_fmt.fail (Printf.sprintf "Tag %s already exists" tag));
    Cn_ffi.Process.exit 1
  end;

  (* Create tag *)
  let tag_ok =
    Cn_ffi.Child_process.exec_in ~cwd:install_dir
      (Printf.sprintf "git tag %s" (Filename.quote tag))
    |> Option.is_some
  in
  if not tag_ok then begin
    print_endline (Cn_fmt.fail "Failed to create tag");
    Cn_ffi.Process.exit 1
  end;

  (* Push tag *)
  let push_ok =
    Cn_ffi.Child_process.exec_in ~cwd:install_dir
      (Printf.sprintf "git push origin %s" (Filename.quote tag))
    |> Option.is_some
  in
  if not push_ok then begin
    print_endline (Cn_fmt.fail "Failed to push tag");
    Cn_ffi.Process.exit 1
  end;

  (* Verify tag on remote *)
  let verified =
    Cn_ffi.Child_process.exec_in ~cwd:install_dir
      (Printf.sprintf "git ls-remote origin refs/tags/%s" (Filename.quote tag))
    |> Option.map (fun s -> String.length (String.trim s) > 0)
    |> Option.value ~default:false
  in
  if not verified then begin
    print_endline (Cn_fmt.fail (Printf.sprintf "Tag %s not found on remote after push" tag));
    Cn_ffi.Process.exit 1
  end;

  (* Create GitHub release via gh CLI *)
  let gh_ok =
    Cn_ffi.Child_process.exec_in ~cwd:install_dir
      (Printf.sprintf "gh release create %s --title %s --notes %s --verify-tag 2>/dev/null"
        (Filename.quote tag)
        (Filename.quote (Printf.sprintf "cn %s" ver))
        (Filename.quote (Printf.sprintf "cn %s\n\nSee CHANGELOG or `git log` for details." ver)))
    |> Option.is_some
  in
  if gh_ok then begin
    print_endline (Cn_fmt.ok (Printf.sprintf "Released %s" tag));
    (match hub_path_opt with Some hp -> Cn_hub.log_action hp "release" (Printf.sprintf "tag:%s version:%s" tag ver) | None -> ())
  end else begin
    print_endline (Cn_fmt.warn (Printf.sprintf "Tag %s pushed but GitHub release failed — create manually or install gh CLI" tag));
    (match hub_path_opt with Some hp -> Cn_hub.log_action hp "release" (Printf.sprintf "tag:%s version:%s gh_release:failed" tag ver) | None -> ())
  end

