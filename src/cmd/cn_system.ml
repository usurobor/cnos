type check_result = { name: string; passed: bool; value: string }

let run_doctor hub_path =
  Printf.printf "cn v%s\n" version;
  print_endline (Cn_fmt.info "Checking health...");
  print_endline "";

  let checks = [
    (match Cn_ffi.Child_process.exec "git --version" with
     | Some v ->
         let trimmed = String.trim v in
         let value = match strip_prefix ~prefix:"git version " trimmed with
           | Some rest -> rest | None -> trimmed in
         { name = "git"; passed = true; value }
     | None -> { name = "git"; passed = false; value = "not installed" });

    (match Cn_ffi.Child_process.exec "git config user.name" with
     | Some v -> { name = "git user.name"; passed = true; value = String.trim v }
     | None -> { name = "git user.name"; passed = false; value = "not set" });

    (match Cn_ffi.Child_process.exec "git config user.email" with
     | Some v -> { name = "git user.email"; passed = true; value = String.trim v }
     | None -> { name = "git user.email"; passed = false; value = "not set" });

    (match Cn_ffi.Child_process.exec "patch --version" with
     | Some v ->
         let line = match String.split_on_char '\n' (String.trim v) with
           | first :: _ -> first | [] -> v in
         { name = "patch"; passed = true; value = String.trim line }
     | None -> { name = "patch"; passed = false;
         value = "not installed (required for fs_patch)" });

    (match Cn_ffi.Child_process.exec "curl --version" with
     | Some v ->
         let line = match String.split_on_char '\n' (String.trim v) with
           | first :: _ -> first | [] -> v in
         let value = match String.split_on_char ' ' line with
           | _ :: ver :: _ -> ver | _ -> String.trim v in
         { name = "curl"; passed = true; value }
     | None -> { name = "curl"; passed = false; value = "not installed (required for agent runtime)" });

    { name = "hub directory"; passed = Cn_ffi.Fs.exists hub_path;
      value = if Cn_ffi.Fs.exists hub_path then "exists" else "not found" };

    (let json_path = Cn_ffi.Path.join hub_path ".cn/config.json" in
     let yaml_path = Cn_ffi.Path.join hub_path ".cn/config.yaml" in
     let has_json = Cn_ffi.Fs.exists json_path in
     let has_yaml = Cn_ffi.Fs.exists yaml_path in
     { name = ".cn/config"; passed = has_json || has_yaml;
       value = if has_json then "config.json"
               else if has_yaml then "config.yaml"
               else "missing" });

    (let p = Cn_ffi.Path.join hub_path "spec/SOUL.md" in
     { name = "spec/SOUL.md"; passed = Cn_ffi.Fs.exists p;
       value = if Cn_ffi.Fs.exists p then "exists" else "missing (optional)" });

    (let p = Cn_ffi.Path.join hub_path "state/peers.md" in
     if Cn_ffi.Fs.exists p then
       let count = Cn_ffi.Fs.read p |> String.split_on_char '\n'
         |> List.filter (fun l -> starts_with ~prefix:"- name:" (String.trim l))
         |> List.length in
       { name = "state/peers.md"; passed = true; value = Printf.sprintf "%d peer(s)" count }
     else { name = "state/peers.md"; passed = false; value = "missing" });

    (match Cn_ffi.Child_process.exec_in ~cwd:hub_path "git remote get-url origin" with
     | Some _ -> { name = "origin remote"; passed = true; value = "configured" }
     | None -> { name = "origin remote"; passed = false; value = "not configured" });

    (* v3.5: Package-based cognitive asset checks *)
    (match Cn_assets.validate_packages ~hub_path with
     | Ok () -> { name = "cnos.core doctrine"; passed = true; value = "present" }
     | Error _ -> { name = "cnos.core doctrine"; passed = false;
         value = "missing (run 'cn setup' or 'cn deps restore')" });

    { name = ".cn/deps.json"; passed = Cn_ffi.Fs.exists (Cn_ffi.Path.join hub_path ".cn/deps.json");
      value = if Cn_ffi.Fs.exists (Cn_ffi.Path.join hub_path ".cn/deps.json")
              then "present" else "missing (run 'cn setup')" };

    { name = ".cn/deps.lock.json";
      passed = Cn_ffi.Fs.exists (Cn_ffi.Path.join hub_path ".cn/deps.lock.json");
      value = if Cn_ffi.Fs.exists (Cn_ffi.Path.join hub_path ".cn/deps.lock.json")
              then "present" else "missing (run 'cn setup')" };

    (* Runtime contract validation — issue #56, #62 (v2 vertical self-model) *)
    { name = "agent/";
      passed = Cn_ffi.Fs.exists (Cn_ffi.Path.join hub_path "agent");
      value = if Cn_ffi.Fs.exists (Cn_ffi.Path.join hub_path "agent")
              then "exists" else "missing (run 'cn setup')" };

    (let contract_path = Cn_ffi.Path.join hub_path "state/runtime-contract.json" in
     if Cn_ffi.Fs.exists contract_path then begin
       let content = Cn_ffi.Fs.read contract_path in
       match Cn_json.parse content with
       | Ok json ->
         (* v2: four layers — identity, cognition, body, medium *)
         let has_identity = Cn_json.get "identity" json <> None in
         let has_cognition = Cn_json.get "cognition" json <> None in
         let has_body = Cn_json.get "body" json <> None in
         let has_medium = Cn_json.get "medium" json <> None in
         let all_ok = has_identity && has_cognition && has_body && has_medium in
         { name = "runtime contract";
           passed = all_ok;
           value = if all_ok then "valid (identity + cognition + body + medium)"
                   else "incomplete" }
       | Error _ ->
         { name = "runtime contract"; passed = false; value = "invalid JSON" }
     end else
       { name = "runtime contract"; passed = false;
         value = "missing (will be created on next wake)" });

    (* v3.15: Binary/package version coherence *)
    (let installed = Cn_assets.list_installed_packages hub_path in
     let mismatched = installed |> List.filter_map (fun (_name, pkg_path) ->
       let dir_name = Filename.basename pkg_path in
       match String.index_opt dir_name '@' with
       | Some i ->
         let v = String.sub dir_name (i + 1) (String.length dir_name - i - 1) in
         if v <> version then Some (dir_name ^ " (expected " ^ version ^ ")") else None
       | None -> None) in
     if mismatched = [] then
       { name = "version coherence"; passed = true;
         value = Printf.sprintf "binary=%s packages=match" version }
     else
       { name = "version coherence"; passed = false;
         value = Printf.sprintf "drift: %s" (String.concat ", " mismatched) });

    (* v3.17: Extension health — discover, check compatibility, host health *)
    (let reg = Cn_extension.build_registry ~hub_path
       ~runtime_version:version () in
     let entries = Cn_extension.all_entries reg in
     let total = List.length entries in
     let enabled_entries = entries |> List.filter (fun e ->
       e.Cn_extension.state = Cn_extension.Enabled) in
     let enabled = List.length enabled_entries in
     let rejected = entries |> List.filter (fun e ->
       match e.Cn_extension.state with
       | Cn_extension.Rejected _ -> true | _ -> false) |> List.length in
     let disabled = entries |> List.filter (fun e ->
       e.Cn_extension.state = Cn_extension.Disabled) |> List.length in
     (* Check host health for enabled extensions *)
     let unhealthy = enabled_entries |> List.filter_map (fun e ->
       let cmd = e.Cn_extension.manifest.Cn_extension.backend.Cn_extension.command in
       match Cn_ext_host.check_health ~command:cmd () with
       | Ok () -> None
       | Error msg -> Some (Printf.sprintf "%s: %s" e.manifest.name msg)
     ) in
     if total = 0 then
       { name = "extensions"; passed = true; value = "none installed" }
     else if rejected > 0 then
       { name = "extensions"; passed = false;
         value = Printf.sprintf "%d installed, %d enabled, %d rejected, %d disabled"
           total enabled rejected disabled }
     else if unhealthy <> [] then
       { name = "extensions"; passed = false;
         value = Printf.sprintf "%d installed, %d enabled (%d unhealthy: %s)"
           total enabled (List.length unhealthy) (String.concat "; " unhealthy) }
     else
       { name = "extensions"; passed = true;
         value = Printf.sprintf "%d installed, %d enabled, %d disabled"
           total enabled disabled });
  ] in

  let width = 22 in
  checks |> List.iter (fun c ->
    let dots = String.make (max 1 (width - String.length c.name)) '.' in
    let status = if c.passed then Cn_fmt.green (Cn_fmt.check ^ " " ^ c.value) else Cn_fmt.red (Cn_fmt.cross ^ " " ^ c.value) in
    Printf.printf "%s%s %s\n" c.name dots status);

  print_endline "";
  let fails = checks |> List.filter (fun c -> not c.passed) |> List.length in
  let oks = List.length checks - fails in
  (if fails = 0 then print_endline (Cn_fmt.ok "All critical checks passed.")
   else print_endline (Cn_fmt.fail (Printf.sprintf "%d issue(s) found." fails)));
  print_endline (Cn_fmt.dim (Printf.sprintf "[status] ok=%d warn=0 fail=%d version=%s" oks fails version))

